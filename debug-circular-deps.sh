#!/bin/bash

# Debug Circular Dependencies in Dashboard
# This script helps identify circular import issues

set -e

cd "$(dirname "$0")"

echo "🔍 Checking for circular dependencies in Dashboard..."
echo ""

# Check if madge is installed
if ! command -v madge &> /dev/null; then
    echo "📦 Installing madge for dependency analysis..."
    npm install -g madge
fi

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "1️⃣  Checking for circular dependencies..."
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
madge --circular --extensions ts,tsx src/

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "2️⃣  Checking component dependency tree..."
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
madge --extensions ts,tsx src/pages/Index.tsx

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "3️⃣  Checking Dashboard page dependencies..."
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
madge --extensions ts,tsx src/pages/Dashboard.tsx

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "4️⃣  Generating dependency graph (image)..."
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
madge --circular --extensions ts,tsx --image circular-deps.png src/
echo "✅ Graph saved to: circular-deps.png"

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "📊 Analysis Complete!"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
